    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:

        String senha_usuario = new String(senhaPasswordField.getPassword());//(getPassword)vetor de char convertido para String
        String nome_usuario = nomeDeUsuarioTextField.getText();//int id_usuario = Usuario.getInstance().getIdUsuario();
        Usuario usuario = UsuarioDAO.obterUsuario(nome_usuario, senha_usuario);
       try{
           if (UsuarioDAO.existe(usuario)){
               JOptionPane.showMessageDialog(null, String.format("Bem vindo, %s!", usuario.getNomeUsuario()));
               DashboardTela dt = new DashboardTela();
               dt.setVisible(true);
               this.dispose();
           }
           else{
               JOptionPane.showMessageDialog(null, String.format("Usuario %s invalido", usuario.getNomeUsuario()));
           }
       }
       catch (Exception e){
           JOptionPane.showMessageDialog(null, "Por favor, tente novamente mais tarde.");
           e.printStackTrace();
       }                     
    }//GEN-LAST:event_loginButtonActionPerformed

