    private void avaliarMusicaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_avaliarMusicaButtonActionPerformed
        // TODO add your handling code here:
        int escolha = JOptionPane.showConfirmDialog(null, "Confirmar avaliação?");
        if (escolha == JOptionPane.YES_OPTION){
            try{
                int id_musica = Integer.parseInt(idAvaliacaoTextField.getText());
                int nota_usuario = Integer.parseInt(avaliarNotaTextField.getText());
                UsuarioAvalia usuarioavalia = new UsuarioAvalia();
                usuarioavalia.setIdMusica(id_musica);
                usuarioavalia.setNotaUsuario(/*3/*usuariotemgenero.getIdGenero()*/nota_usuario);
                usuarioavalia.setIdUsuario(/*1*/Usuario.getInstance().getIdUsuario());
                UsuarioAvaliaDAO.avaliarMusica(usuarioavalia);
                UsuarioAvaliaDAO.atualizarMediaMusica(usuarioavalia);
                JOptionPane.showMessageDialog(null, "Música avaliada");
                buscarMusicas();
                buscarMinhasAvaliacoes();
                avaliarNotaTextField.setText("");
                reavaliarNotaTextField.setText("");
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null, "Algo deu errado. Tente novamente mais tarde.");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Tá bom.");
        }
    }//GEN-LAST:event_avaliarMusicaButtonActionPerformed

