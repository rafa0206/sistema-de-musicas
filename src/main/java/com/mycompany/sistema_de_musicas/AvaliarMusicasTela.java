    private void buscarMusicas (){
        try{
            List <Musica> musicas = MusicaDAO.listarMusicas(Usuario.getInstance());
            escolhaAMusicaComboBox.setModel(new DefaultComboBoxModel<>(musicas.toArray(new Musica[0])));
            
        }
        catch (Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Tente novamente mais tarde");
        }
    }
    
    private void buscarMinhasAvaliacoes (){
        try{
            List <Musica> musicas = MusicaDAO.listarMinhasAvaliacoes(Usuario.getInstance());
            minhasAvaliacoesComboBox.setModel(new DefaultComboBoxModel<>(musicas.toArray(new Musica[0])));
            
        }
        catch (Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Tente novamente mais tarde");
        }
    }

